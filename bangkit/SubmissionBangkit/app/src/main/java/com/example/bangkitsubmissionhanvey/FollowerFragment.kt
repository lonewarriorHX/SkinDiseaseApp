import com.example.bangkitsubmissionhanvey.model.DataFollowers
import com.example.bangkitsubmissionhanvey.model.DataUsers
import com.example.bangkitsubmissionhanvey.presenter.ListDataFollowersAdapter
import com.example.bangkitsubmissionhanvey.presenter.followersFilterList
